  USERS: [DATA_SYNC_EVENTS.REFRESH_USERS, DATA_SYNC_EVENTS.USER_REGISTERED],
  SHELTERS: [DATA_SYNC_EVENTS.REFRESH_SHELTERS, DATA_SYNC_EVENTS.USER_REGISTERED],
  VETS: [DATA_SYNC_EVENTS.REFRESH_VETS, DATA_SYNC_EVENTS.USER_REGISTERED],
  PETS: [DATA_SYNC_EVENTS.REFRESH_PETS, DATA_SYNC_EVENTS.PET_CREATED, DATA_SYNC_EVENTS.PET_UPDATED, DATA_SYNC_EVENTS.PET_DELETED, DATA_SYNC_EVENTS.PET_STATUS_CHANGED],
  APPLICATIONS: [DATA_SYNC_EVENTS.REFRESH_APPLICATIONS, DATA_SYNC_EVENTS.ADOPTION_CREATED, DATA_SYNC_EVENTS.ADOPTION_UPDATED, DATA_SYNC_EVENTS.ADOPTION_APPROVED, DATA_SYNC_EVENTS.ADOPTION_REJECTED],
  COMPLAINTS: [DATA_SYNC_EVENTS.REFRESH_COMPLAINTS, DATA_SYNC_EVENTS.COMPLAINT_CREATED, DATA_SYNC_EVENTS.COMPLAINT_UPDATED, DATA_SYNC_EVENTS.COMPLAINT_RESOLVED],
