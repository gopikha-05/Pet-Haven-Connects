import { useCallback } from 'react';
import { useDataRefresh, DATA_SYNC_EVENTS } from '@/context/DataSyncContext';

export function useAutoRefresh(events, refetchCallback, deps = []) {
  useDataRefresh(
    Array.isArray(events) ? events : [events],
    useCallback(() => {
      if (refetchCallback) {
        refetchCallback();
      }
    }, [refetchCallback, ...deps])
  );
}

